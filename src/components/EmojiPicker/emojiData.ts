// Helper to apply skin tone to an emoji
export const applySkintone = (emoji: string, skinTone: SkinToneId): string => {
  if (skinTone === 'default' || !SKIN_TONE_SUPPORTED_EMOJIS.has(emoji)) {
    return emoji;
  }
  const modifier = SKIN_TONES.find((t) => t.id === skinTone)?.modifier || '';
  // Insert skin tone modifier after the base emoji
  return emoji + modifier;
};

