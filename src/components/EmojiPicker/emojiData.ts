// Search emojis by keyword
export const searchEmojis = (query: string): string[] => {
  if (!query.trim()) return [];

  const lowerQuery = query.toLowerCase();
  const results: string[] = [];
  const seen = new Set<string>();

  // Search in keywords
  for (const [emoji, keywords] of Object.entries(EMOJI_KEYWORDS)) {
    if (keywords.some((k) => k.includes(lowerQuery))) {
      if (!seen.has(emoji)) {
        results.push(emoji);
        seen.add(emoji);
      }
    }
  }

  // Search in category emojis (basic character match)
  for (const category of EMOJI_CATEGORIES) {
    if (category.id === 'recent') continue;
    for (const emoji of category.emojis) {
      if (!seen.has(emoji) && results.length < 50) {
        // Check if the emoji name might match
        const emojiLower = emoji.toLowerCase();
        if (emojiLower.includes(lowerQuery)) {
          results.push(emoji);
          seen.add(emoji);
        }
      }
    }
  }

  return results.slice(0, 50);
};

